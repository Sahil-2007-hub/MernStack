// While Loop 
console.log("While");
let num = 0;
while(num <= 10) {
    console.log(`Number is ${num}`);
    num++;
}

// Do-While Loop
console.log("Do-While");
let count = 0;
do {
    console.log(`Count is ${count}`);
    count++;
}while(count>=10)

// For Loop
for(i=2; i<=10; i+=2)
{
    console.log(i)
}

